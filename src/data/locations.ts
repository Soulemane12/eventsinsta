export function searchLocations(query: string): Location[] {
  if (!query.trim()) return []
  
  const searchTerm = query.toLowerCase().trim()
  
  return LOCATIONS.filter(location => {
    const nameMatch = location.name.toLowerCase().includes(searchTerm)
    const stateMatch = location.state?.toLowerCase().includes(searchTerm)
    const displayMatch = location.displayName.toLowerCase().includes(searchTerm)
    
    return nameMatch || stateMatch || displayMatch
  }).slice(0, 10) // Limit to 10 results
}
